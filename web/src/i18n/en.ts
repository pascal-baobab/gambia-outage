// Widen the `as const` literals: strings → string, numbers → number, while preserving function
// signatures and the nested object shape. Other-language dictionaries implement this widened contract.
type Widen<T> = T extends (...args: infer A) => infer R
  ? (...args: A) => R
  : T extends string
    ? string
    : T extends number
      ? number
      : T extends boolean
        ? boolean
        : { [K in keyof T]: Widen<T[K]> }

export type Strings = Widen<typeof en>
